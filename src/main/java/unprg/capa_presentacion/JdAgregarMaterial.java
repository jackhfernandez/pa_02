    private void cargarEstilos() {
        UiHelper.estilarCampoForm(txtBuscar, "Buscar material...");
    }

    private void pasarDatos(String producto, int ctd) {
        try {
            Material m = materialDAO.getElemento(producto);
            
            if (m != null) {
                int stock = m.getStock();
                
                if (ctd <= stock) {
                    material = producto;
                    cant = ctd;
                    
                    String descuento = JOptionPane.showInputDialog(rootPane, "Ingrese el porcentaje", "0");
                    
                    if (descuento != null) {
                        try {
                            desc = Integer.parseInt(descuento);
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(rootPane, "El descuendo debe ser un numero valido");
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Stock insuficiente. Disponible " + stock);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Material no encontrado en la lista");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Error: " + e.getMessage());
        }
    }
    
    private void tblListadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListadoMouseClicked
        if (evt.getClickCount() == 1) {
            int filaSeleccionada = tblListado.getSelectedRow();
            if (filaSeleccionada != -1) {
                String producto = String.valueOf(tblListado.getValueAt(filaSeleccionada, 0));
                
                String ctd = JOptionPane.showInputDialog(rootPane, "Ingrese la cantidad: ", "1");
                
                if (ctd != null && !ctd.trim().isEmpty()) {
                    try {
                        int cantidad = Integer.parseInt(ctd);
                        if (cantidad > 0) {
                            pasarDatos(producto, cantidad);
                            this.dispose();
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "La cantidad debe ser mayor a 0");
                        }
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(rootPane, "Cantidad no v√°lida");
                    }
                }
            }
        }
    }//GEN-LAST:event_tblListadoMouseClicked

